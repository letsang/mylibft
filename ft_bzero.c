#include "libft.h"

void	ft_bzero(void *s, size_t n)
{
	size_t	i;
	char	*ps;

	i = 0;
	ps = (char*)s;
	if (n == 0)
		return ;
	while (i < n)
	{
		ps[i] = '\0';
		i++;
	}
}
