#include "libft.h"

char	*ft_strmapi(const char *s, char (*f)(unsigned int, char))
{
	int		i;
	int		len;
	char	*map;

	if (!s)
		return (NULL);
	len = ft_strlen(s) + 1;
	if (!(map = (char *)malloc(len * sizeof(char))))
		return (NULL);
	i = 0;
	while (s[i])
	{
		map[i] = (*f)(i, s[i]);
		i++;
	}
	map[i] = '\0';
	return (map);
}
